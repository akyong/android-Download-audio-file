package com.binus.ind.cloud_computing_binus.domain;

public class Language {
    private String name;
    private String voice;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setVoice(String voice) {
        this.voice = voice;
    }

    public String getVoice() {
        return voice;
    }
}
